/*
* 변수 초기화
* int, double, String, char, boolean
* ⭐️ 자료형 변수명 = 값;
*
* 1. 정수 3을 변수 a에 저장
* 2. 실수 3.14를 변수 b에 저장
* 3. 문자열 hello를 변수 c에 저장
* 4. 문자 A를 변수 d에 저장
* 5. 불린 true를 변수 e에 저장
* */
public class Main {
    public static void main(String[] args) {

        int a = 3;
        double b = 3.14;
        String c = "hello";
        char d = 'A';
        boolean e = true;

        System.out.println(a + "\n" + b + "\n" + c + "\n" + d + "\n" + e);
    }
}